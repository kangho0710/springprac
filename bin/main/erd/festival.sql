SET SESSION FOREIGN_KEY_CHECKS=0;

/* Drop Tables */

DROP TABLE IF EXISTS FESTIVAL_INFO;




/* Create Tables */

CREATE TABLE FESTIVAL_INFO
(
	NUM int NOT NULL AUTO_INCREMENT,
	ADDR1 varchar(300) NOT NULL,
	ADDR2 varchar(300),
	BOOKTOUR varchar(300),
	CAT1 varchar(300),
	CAT2 varchar(300),
	CAT3 varchar(300),
	CONTENTID varchar(300) NOT NULL,
	CONTENTTYPEID varchar(300) NOT NULL,
	CREATEDTIME char(13) NOT NULL,
	EVENTSTARTTIME char(8) NOT NULL,
	FIRSTIMAGE varchar(400),
	FIRSTIMAGE2 varchar(400),
	MAPX varchar(300) NOT NULL,
	MAPY varchar(300),
	MLEVEL char(2),
	MODIFIEDTIME char(15) NOT NULL,
	READCOUNT int(10),
	AREACODE char(3) NOT NULL,
	SIGUNGUCODE varchar(300),
	TEL char(13) NOT NULL,
	TITLE varchar(400) NOT NULL,
	PRIMARY KEY (NUM),
	UNIQUE (CONTENTID)
);



